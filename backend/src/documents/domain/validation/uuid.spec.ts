import { isUuid } from './uuid';

describe('isUuid', () => {
  it.each([
    '22222222-2222-4222-8222-222222222222',
    'AAAAAAAA-AAAA-4AAA-8AAA-AAAAAAAAAAAA',
    '22222222-2222-1222-8222-222222222222',
    '22222222-2222-5222-B222-222222222222',
  ])('accepts %s', (value) => {
    expect(isUuid(value)).toBe(true);
  });

  it.each([
    'document-1',
    ' 22222222-2222-4222-8222-222222222222',
    '22222222-2222-4222-8222-222222222222 ',
    '22222222-2222-4222-8222-22222222222',
    '22222222-2222-4222-8222-2222222222220',
    'g2222222-2222-4222-8222-222222222222',
    '22222222-2222-0222-8222-222222222222',
    '22222222-2222-6222-8222-222222222222',
    '22222222-2222-4222-7222-222222222222',
  ])('rejects %s', (value) => {
    expect(isUuid(value)).toBe(false);
  });
});
